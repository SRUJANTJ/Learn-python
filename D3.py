# Range



#One argument
for i in range(5):
 print("Once argument",i)
# Output: 0, 1, 2, 3, 4 

#Two argument
for i in range(2, 6):
 print("Two argument",i)
# Output: 2, 3, 4, 5

#Three argument
for i in range(1, 10, 2):
 print("Three argument",i)
# Output: 1, 3, 5, 7, 9



