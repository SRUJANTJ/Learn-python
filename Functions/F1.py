# Defining a function
# 'def' is used to define the function
# 'function()' here is the name of the function, but it can be any valid name
# The parentheses '()' mean it's a function and can hold arguments

def function():
    print('hello')

# Calling the function
function()







# Examples of Python built-in functions

# print() - Outputs to the console
print('Hello, World!')

# len() - Returns the length of an object
my_list = [1, 2, 3, 4]
print(len(my_list))  # Output: 4

# type() - Returns the type of a variable
num = 10
print(type(num))  # Output: <class 'int'>

# sum() - Returns the sum of an iterable
numbers = [1, 2, 3, 4]
print(sum(numbers))  # Output: 10

# max() - Returns the largest item in an iterable
print(max(numbers))  # Output: 4

# min() - Returns the smallest item in an iterable
print(min(numbers))  # Output: 1

# sorted() - Returns a sorted list from the specified iterable
print(sorted(numbers))  # Output: [1, 2, 3, 4]

# str() - Converts a value to a string
value = 123
print(str(value))  # Output: '123'

# abs() - Returns the absolute value of a number
negative_number = -5
print(abs(negative_number))  # Output: 5